def encode(string):
    output = []
    for char in string:
        morse = char_to_morse.get(char, None)
        if morse:
            output.append(morse)
    return output


def decode(morse):
    output = []
    for string in morse:
        char = morse_to_char.get(string, None)
        if char:
            output.append(char)
    return output
